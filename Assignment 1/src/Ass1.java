/*  Question: 
 
 Write a simple program which accepts 4 values for the variables ,
 use Logical operators to print their results And arithmetic operators for addition subtraction ,
 multiplication and the division with their respective results each on new line.
 
 */



import java.util.Scanner;

public class Ass1 {
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		
		//declaring 4 variables 
		
		int a;
		int b;
		int c;
		int d;
		
		
		//declaring the results and initializing them with the values.
		
		int sum = 0;
		int  diff = 0;
		int product = 0;
		double div = 0.0;
		
		
		//taking user inputs
		
		System.out.println("Enter 4 Numbers: ");
		
		a = sc.nextInt();
		b = sc.nextInt();
		c = sc.nextInt();
		d = sc.nextInt();
		
		
		//Task 1 : Addition of 4 numbers 
		
		sum = a+b+c+d;
		
		System.out.println("Sum of All the Numbers is : "+ sum);
		
		
		
		//Task 2 : Subtraction of 4 numbers : Solving for only two numbers at a time
		
		 // Subtraction of a and b
		if(a>b) { 
			diff = a-b;
			System.out.println("Substraction of "+ a +" and " +b+ " is: "+ diff);
		}
		else {
			diff = b -a;
			System.out.println("Substraction of "+ b +" and " +a+ " is: "+ diff);
		}
		
		
		// Subtraction of c and d
		if(c>d) {
			diff = c-d;
			System.out.println("Substraction of "+ c +" and " +d+ " is: "+ diff);
		}
		else {
			diff = d-c;
			System.out.println("Substraction of "+ d +" and " +c+ " is: "+ diff);
		}

		
		
		//Task 3 : Multiplication of 4 numbers 
		
		product = a*b*c*d;
		
		System.out.println("Mupliplication of All the Numbers is : "+ product);
		
		
		
		//Task 4 : division of 4 numbers : Solving for only two numbers at a time
		
		// Division of a and b

		if(a>b) {
			div = a/b;
			System.out.println("division of "+ a +" and " +b+ " is: "+ div);
		}
		else{
			div = b/a;
			System.out.println("division of "+ b +" and " +a+ " is: "+ div);
		}
		
		// Division of c and d
		if(c>d) {
			div = c/d;
			System.out.println("division of "+ c +" and " +d+ " is: "+ div);
		}
		else {
			div = d/c;
			System.out.println("division of "+ d +" and " +c+ " is: "+ div);
		}

	}
	

}
